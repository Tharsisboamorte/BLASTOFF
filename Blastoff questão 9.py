num = int(input("Escolha um número: "))
vez = 1
print("tabuada da multiplicação")
while vez <= 10:
    print( num, "x", vez, "=", num*vez)
    vez = vez + 1
vez = 1
print("tabuada da soma")
while vez <= 10:
    print( num, "+", vez, "=", num+vez)
    vez = vez + 1
vez = 1
print("tabuada da subtração")
while vez <= 10:
    print( num, "-", vez, "=", num-vez)
    vez = vez + 1
vez = 1
print("tabuada da divisão")
while vez <= 10:
    print (num, "/", vez, "=", num/vez)
    vez = vez + 1